# list methods
l1=[12,98,45,23,90,10]
l1.sort()
print(l1)
l1.reverse()
print(l1)
l1.append(67)  #-->adds something at the end of the string
print(l1)
l1.insert(3,20)  #-->adds 20 at index 3   syntax:(index no.,no. to be added in list)
print(l1)
l1.pop(2)   #-->deletes the element of the 2nd index
print(l1)
l1.remove(67)  #-->removes 67 from the list
print(l1)