for i in range(8):  #will print upto n-1 terms.By default i starts from zero
   print(i)

print('\n')
for i in range(1,8):  #here 1 indicates that i=1 and so the loop will initiate from 1
   print(i)

print('\n')
for i in range(2,8): #loop is initialized from i=2
   print(i)

print('\n')
for i in range(1,8,2): #will print value with the jump of 2 digit
    print(i)

#therefore the syntax of range is :
