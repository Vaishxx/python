import turtle
bob=turtle.Turtle()
bob.color('red','pink')   

bob.begin_fill()
bob.forward(200)
bob.left(90)
bob.forward(200)
bob.left(90)
bob.forward(200)
bob.left(90)
bob.forward(200)

bob.penup()
bob.forward(200)
bob.pendown()

bob.forward(200)
bob.left(90)
bob.forward(200)
bob.left(90)
bob.forward(200)
bob.left(90)
bob.forward(200)
bob.end_fill()

turtle.done()